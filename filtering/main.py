import cv2
import numpy as np


def BGR2GRAY(src: np.ndarray) -> np.ndarray:
    '''将RGB图转为8位灰度图'''

    tmp = src.astype(np.uint32)  # 用32位整型存储防止溢出

    # RGB to Gray: Y←0.299⋅R+0.587⋅G+0.114⋅B
    return ((tmp[:, :, 2]*299 + tmp[:, :, 1]*587 + tmp[:, :, 0]*114+500)//1000).astype(np.uint8)


def convertScaleAbs(src: np.ndarray) -> np.ndarray:
    '''取绝对值并截断为int8'''
    return np.abs(src).clip(0, 255).astype(np.uint8)  # 和OpenCV算法保持一致


def replicateBorder(src: np.ndarray, pad: int) -> np.ndarray:
    '''图像边缘填充'''

    height, width = src.shape  # 获得原图长宽

    dst = np.empty((height+pad*2, width+pad*2), dtype=src.dtype)  # 分配空间
    dst[pad: height+pad, pad: width+pad] = src.copy()  # 载入原图

    # pad left
    dst[pad: height+pad, 0:pad] = np.tile(
        dst[pad: height+pad, pad].reshape(-1, 1), (1, pad))

    # pad right
    dst[pad: height+pad, width+pad:width+pad*2] = np.tile(
        dst[pad: height+pad, width+pad-1].reshape(-1, 1), (1, pad))

    # pad top
    dst[0:pad, 0:width+pad * 2] = np.tile(
        dst[pad, 0:width+pad*2].reshape(1, -1), (pad, 1))

    # pad bottom
    dst[height+pad:height + pad*2, 0:width+pad * 2] = np.tile(
        dst[height+pad-1, 0:width+pad*2].reshape(1, -1), (pad, 1))

    return dst  # return result


def linearFilter(src: np.ndarray, kernal: np.ndarray, kernal1: np.ndarray = None, dtype: np.dtype = np.float64) -> np.ndarray:
    '''线性滤波器'''

    height, width = src.shape  # 获得图像长宽
    kerlSize = kernal.shape[0]  # 卷积核大小
    imgPadded = replicateBorder(src,  kerlSize//2)  # 边缘填充
    # allocate memory for result,note that the default type is float64 to ensure accuracy
    dst = np.empty_like(src, dtype=dtype)

    if kernal1 is not None:  # 若传入分解的卷积核
        # filtering
        for y in range(height):  # 对所有行
            for x in range(width):  # 对所有列
                dst[y, x] = (kernal*kernal1*(imgPadded[y: y + kerlSize,
                             x: x + kerlSize])).sum(dtype=dtype)  # 卷积
    else:  # 否则
        # filtering
        for y in range(height):  # 对所有行
            for x in range(width):  # 对所有列
                dst[y, x] = (
                    kernal*(imgPadded[y: y + kerlSize, x: x + kerlSize])).sum(dtype=dtype)  # 卷积

    return dst  # return result


def SobelFilter(src: np.ndarray) -> np.ndarray:
    '''Sobel滤波'''

    # x方向Sobel算子
    kerlX = np.array([[1],
                     [2],
                     [1]])
    kerlX1 = np.array([1, 0, -1])

    # y方向Sobel算子
    kerlY = np.array([[1],
                     [0],
                     [-1]])
    kerlY1 = np.array([1, 2, 1])

    # 构造x方向Sobel滤波器，注意到未归一化，和OpenCV的实现保持一致
    dstX = convertScaleAbs(linearFilter(src, kerlX, kerlX1, dtype=np.int16))
    # 构造y方向Sobel滤波器，注意到未归一化，和OpenCV的实现保持一致
    dstY = convertScaleAbs(linearFilter(src, kerlY, kerlY1, dtype=np.int16))

    return dstX, dstY  # return result


def derivationFilter(src: np.ndarray) -> np.ndarray:
    '''导数滤波'''

    # x方向导数算子
    kerlX = np.array([[0],
                      [1],
                      [0]])
    kerlX1 = np.array([1, 0, -1])

    # y方向导数算子
    kerlY = np.array([[1],
                      [0],
                      [-1]])
    kerlY1 = np.array([0, 1, 0])

    # 构造x方向导数滤波器，注意到未归一化，和OpenCV的实现保持一致
    dstX = convertScaleAbs(linearFilter(src, kerlX, kerlX1, dtype=np.int16))
    # 构造y方向导数滤波器，注意到未归一化，和OpenCV的实现保持一致
    dstY = convertScaleAbs(linearFilter(src, kerlY, kerlY1, dtype=np.int16))

    return dstX, dstY  # return result


def medianFilter(src: np.ndarray, kSize: int = 3) -> np.ndarray:
    '''中值滤波'''

    height, width = src.shape  # 获得图像长宽
    imgPadded = replicateBorder(src,  kSize//2)  # 边缘填充
    dst = np.empty_like(src, dtype=src.dtype)  # allocate memory for result

    # filtering
    for y in range(height):  # 对所有行
        for x in range(width):  # 对所有列
            # 选取窗口中像素值的中位数
            dst[y, x] = np.median(imgPadded[y: y + kSize, x: x + kSize])

    return dst  # return result


def boxFilter(src: np.ndarray, kSize: int = 3, dtype: np.dtype = np.uint8) -> np.ndarray:
    '''均值滤波'''

    # 均值算子
    kerl = np.ones(kSize, dtype=src.dtype).T
    kerl1 = np.ones(kSize, dtype=src.dtype)

    # 构造均值滤波器
    if dtype == np.uint8:
        dst = (linearFilter(src, kerl, kerl1, dtype=np.int16) /
               kSize**2).astype(dtype)  # 为图像处理优化
    else:  # 否则
        dst = (linearFilter(src, kerl, kerl1) / kSize**2).astype(dtype)  # 一般情况

    return dst  # return result


def GaussianFilter(src: np.ndarray, kSize: int, sigma: float) -> np.ndarray:
    '''高斯滤波'''

    kerl = np.empty((kSize, kSize), dtype=np.float16)  # 为卷积核分配空间
    cter = kSize // 2  # 卷积核的中心位置

    for i in range(kSize):  # 对卷积核的每行
        for j in range(kSize):  # 对卷积核的每列
            # 计算卷积核中每个元素的值，注意到(1/2πσ)系数的计算是不必要的，因为会在归一化中被消去
            kerl[i][j] = np.exp(-((i-cter)**2+(j-cter)**2) / (2*sigma**2))

    kerl /= kerl.sum()  # 归一化，避免影响图像明度

    dst = linearFilter(src, kerl, dtype=src.dtype)  # 构造高斯滤波器

    return dst  # return result


def bilateralFilter(src: np.ndarray, kSize: int, sigmaSpacial: float, sigmaRange: float) -> np.ndarray:
    '''双边滤波'''

    height, width = src.shape  # 获得图像长宽
    cter = kSize // 2  # 卷积核的中心位置

    kerlS = np.empty((kSize, kSize), dtype=np.float16)  # 为空间域核分配空间
    kerlR = np.empty((kSize, kSize), dtype=np.float16)  # 为值域核分配空间
    kerl = np.empty((kSize, kSize), dtype=np.float16)  # 为卷积核分配空间

    imgPadded = replicateBorder(src,  kSize//2).astype(np.int16)  # 边缘填充
    dst = np.empty_like(src, dtype=src.dtype)  # allocate memory for result

    # 空间域核对每像素相同，先行计算
    for i in range(kSize):  # 对空间域核的每行
        for j in range(kSize):  # 对空间域核的每列
            # 计算空间域核中每个元素的值，注意到(1/2πσ)系数的计算是不必要的，因为会在归一化中被消去
            kerlS[i][j] = np.exp(-((i-cter)**2+(j-cter)**2) /
                                 (2*sigmaSpacial**2), dtype=np.float64)

    # 为KerlR的lookup table分配空间
    lutKerlR = np.empty((256, 256), dtype=kerlR.dtype)
    for i in range(256):
        for j in range(256):
            # 预先计算出值域核中所有可能的权重，方便后期查表，注意到(1/2πσ)系数的计算是不必要的，因为会在归一化中被消去，后面系数为提高精度
            lutKerlR[i, j] = np.exp(-(i-j)**2 / (2*sigmaRange**2), dtype=np.float64) * \
                min(np.finfo(kerl.dtype).max, np.finfo(kerlR.dtype).max)

    # filtering
    for y in range(height):  # 对所有行
        for x in range(width):  # 对所有列
            # 计算值域核
            for i in range(kSize):  # 对值域核的每行
                for j in range(kSize):  # 对值域核的每列
                    # 查表得到值域核中每个元素的值，注意到(1/2πσ)系数的计算是不必要的，因为会在归一化中被消去
                    kerlR[i, j] = lutKerlR[imgPadded[y+i, x+j],
                                           imgPadded[y+cter, x+cter]]

            kerl = kerlS*kerlR  # 计算卷积核
            kerl /= kerl.sum(dtype=float)  # 归一化卷积核，避免影响图像明度

            dst[y, x] = (
                kerl*(imgPadded[y: y + kSize, x: x + kSize])).sum()  # 卷积

    return dst   # return result


def guidedFilter(src: np.ndarray, kSize: int, eps: float, guide: np.ndarray = None) -> np.ndarray:
    '''导向滤波'''

    srcCopy = src.astype(np.float16)  # 类型转换，避免溢出
    eps *= 255**2  # 为和归一化的值有可比性

    if guide is None:  # 若未指定引导图像
        guide = srcCopy  # 取引导图像为输入图像

    meanp = boxFilter(srcCopy, kSize, dtype=np.float16)  # 生成待滤波图像均值
    meanI = boxFilter(guide, kSize, dtype=np.float16)  # 生成引导图像均值
    meanIp = boxFilter(srcCopy*guide, kSize, dtype=np.float16)  # 生成互相关均值
    meanII = boxFilter(guide*guide, kSize, dtype=np.float16)  # 生成引导图像自相关均值

    covIp = meanIp-meanI*meanp  # Ip的协方差
    varI = meanII-meanI*meanI  # I的方差

    a = covIp/(varI+eps)  # 参数系数a
    b = meanp-a*meanI  # 参数系数b

    meana = boxFilter(a, kSize, dtype=np.float16)  # 系数a的均值
    meanb = boxFilter(b, kSize, dtype=np.float16)  # 系数b的均值

    dst = meana*srcCopy+meanb  # 生成输出矩阵

    return dst.astype(src.dtype)  # 转换类型并返回


